def quadrado(lado):
    area = lado ** 2
    return area 
print(quadrado(50))

def reajuste(salario):
        novo = salario + salario * 0.15
        return novo 
print(reajuste(1000)) 

def triangulo(base, altura):
        area=(base * altura)/2
        return area 
print(triangulo(2,2))

def celsius_para_fahrenheit(celsius):
        area= (9 * celsius + 160) / 5
        return area 
print(celsius_para_fahrenheit(100))


    

